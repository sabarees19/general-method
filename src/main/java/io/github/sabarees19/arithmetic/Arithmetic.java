package io.github.sabarees19.arithmetic;

public class Arithmetic {

    public int add(int int1, int int2) {
        return int1 + int2;
    }

    public int minus(int int1, int int2) {
        return int1 - int2;
    }

    public int multiply(int int1, int int2) {
        return int1 * int2;
    }

    public int divide(int int1, int int2) {
        return int1 / int2;
    }

}
